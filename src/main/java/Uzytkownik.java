import javafx.beans.property.IntegerProperty;
import javafx.beans.property.SimpleIntegerProperty;
import javafx.beans.property.SimpleStringProperty;
import javafx.beans.property.StringProperty;

/**
 * Class Uzytkownik contains constructor and getters and setters to handle all the user's properties like setting
 * ID, login, pasword and the type of new user
 */

public class Uzytkownik {

    private IntegerProperty id;
    private StringProperty login;
    private StringProperty haslo;
    private StringProperty rodzaj;

    /**
     * empty constructor
     */
    public Uzytkownik() {
        id = new SimpleIntegerProperty();
        login = new SimpleStringProperty();
        haslo = new SimpleStringProperty();
        rodzaj = new SimpleStringProperty();
    }

    public int getId() {
        return id.get();
    }

    public IntegerProperty idProperty() {
        return id;
    }

    public void setId(int id) {
        this.id.set(id);
    }

    public String getLogin() {
        return login.get();
    }

    public StringProperty loginProperty() {
        return login;
    }

    public void setLogin(String login) {
        this.login.set(login);
    }

    public String getHaslo() {
        return haslo.get();
    }

    public StringProperty hasloProperty() {
        return haslo;
    }

    public void setHaslo(String haslo) {
        this.haslo.set(haslo);
    }

    public String getRodzaj() {
        return rodzaj.get();
    }

    public StringProperty rodzajProperty() {
        return rodzaj;
    }

    public void setRodzaj(String rodzaj) {
        this.rodzaj.set(rodzaj);
    }
}
